# El while funciona de la misma manera que el for-in, con la diferencia que
# se ejecutará elemento a elemento siempre y cuando la condición se cumpla
# ilimitadas veces, y se escribe de la siguiente forma, ejemplo:

num=1
while num<5:
    print(num)
    num+=1
 # Se ejecuta hasta el n° 4 porque el 5 ya no cumple la condición
 