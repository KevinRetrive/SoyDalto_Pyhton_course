#Son los de siempre + - * y /

#Es importante remarcar que hay que tener en cuenta cuando trabajamos con n° no enteros o flotantes (floats)
#Para saber que tipo de dato tengo uso la función type

#Elevado es **
#Módulo (Es el resto de una división) se escribe: n° % n°
#División baja (me hace la división pero me deja solo la arte entera) se escribe: n° // n°

print(3**2) #9
print(33/2) #16.5
print(33//2) #Sólo devuelve la parte entera 16 (o de otro punto de vista, redondea siempre hacia abajo)
print(33%2) #El resto es 1

tipo_de_dato = type(5) #dice que tipo de dato es str (string), int, float, tupla, lists, etc.
print(tipo_de_dato)
