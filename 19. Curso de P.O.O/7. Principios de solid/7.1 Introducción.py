# Las principales caracteristicas que tenemos que tener en cuenta a la hora de crear un código
# para una empresa o proyecto importante, en donde el código va a ir creciendo, o modificandose, son:

# 1. Mantenibilidad --> Fácil de mantener y modificar en el tiempo (código organizado), una estructura clara

# 2. Reusabilidad --> Todos los componentes reusables (módulos, clases, funciones), todo tiene que ser independiente,
# evitando el esfuerzo y la duplicación innecesaria

# 3. Legibilidad --> Claro para entender y leer, comentarios, seguir convenciones y estructuras

# 4. Extensibilidad --> Debe poder extenderse sin afectar su funcionamiento

# Principios SOLID (según uncle Bob) para buenas prácticas

# - S: SRP, principio de responsabilidad único
# - O: OCP, principio de abierto/cerrado
# - L: LSP, principio de sustitución de Liskov
# - I: ISP, principio de segregación de interfaz
# - D: DIP, principio de inversión de dependencia

# OBS: estos principios fueron creados basandose en lenguajes de tipo estático (Java, C++), sin embargo, 
# son igual de aplicables para el lenguaje de Py, tipo dinámico

