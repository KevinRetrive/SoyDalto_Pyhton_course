# POO (Programación orientada a objetos)
# Es una forma más de programar, para desarrollar softwares sobre las bases de clase u objeto
# Cada objeto es como una instancia de una clase
# Y la clase es como una especie de plantilla que creamos que define las propiedades (atributos) y acciones (métodos) de los objetos

# Todo esto sirve para darle estructura y organización al código y a su vez más "simple" y contruir código más complejos
# Sirve para no tener que re-prgramar  y que sean más seguros... por ejemplo
# Si hice una sopa, no tengo que andar haciendo la receta cada vez aue la vuelva a cocinar, porque la receta (clase) ya esta definida para las sopas (objetos)

# Ver el video de POO antes de continuar con el curso de Py. 7:11 hs
# Lo que nosotros aprendios hasta ahora en el curso de Py, es a programar de forma secuencial, instrucción tras instrucción, sin paradigmas
# Sólo a usar las herramientas, ahora tengo que ver como usarlo para desarrollar cosas o hallar soluciones de la mejor manera posible

# En resumen la POO es la manera ideal de resolver problemas complejo, ya que realizar programción secuencial nos llevaría muchisimas líneas
# Un problema complejo puede ser tanto como desde crear una página web hasta la IA
